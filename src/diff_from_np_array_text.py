import numpy as np

def text_to_array(text):
    # numpyのarray-likeな文字列を正しく配列化
    # 1. 改行・カンマ・カッコなどで整形
    text = text.replace('[', '').replace(']', '')
    rows = text.strip().split('\n')
    array = [list(map(float, row.strip().split())) for row in rows if row.strip()]
    return np.array(array)

